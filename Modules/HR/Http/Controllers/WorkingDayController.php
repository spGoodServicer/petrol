    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            $business_id = request()->session()->get('user.business_id');

            $days = $request->except('_token');
            WorkingDay::where('business_id', $business_id)->update(['flag' => 0]);
            foreach ($days as $key => $value) {
                WorkingDay::updateOrCreate(['business_id' => $business_id, 'days' => $key], ['business_id' => $business_id, 'days' => $key, 'flag' => array_key_exists($key, $days) ? 1 : 0]);
            }

            $output = [
                'success' => 1,
                'tab' => 'working_days',
                'msg' => __('hr::lang.working_days_update_success')
            ];
        } catch (\Exception $e) {
            Log::emergency("File:" . $e->getFile() . "Line:" . $e->getLine() . "Message:" . $e->getMessage());

            $output = [
                'success' => 0,
                'tab' => 'working_days',
                'msg' => __('messages.something_went_wrong')
            ];
        }

        return redirect()->back()->with('status', $output);
    }

