    /**
     * Getting the order created by customer
     *
     * 
     * @return \Illuminate\Http\Response
     */
    public function getOrders()
    {
        if (auth()->user()->is_customer == 0 ) {
            abort(403, 'Unauthorized action.');
        }
        $contact_id = Contact::where('contact_id', auth()->user()->username)->first()->id;
        if (request()->ajax()) {
            $business_id = request()->session()->get('user.business_id');
            $is_quotation = request()->only('is_quotation', 0);

            $sells = Transaction::leftJoin('contacts', 'transactions.contact_id', '=', 'contacts.id')
                ->join(
                    'business_locations AS bl',
                    'transactions.location_id',
                    '=',
                    'bl.id'
                )
                ->where('transactions.business_id', $business_id)
                ->where('transactions.type', 'sell')
                ->where('is_customer_order', 1)
                ->where('contacts.id', $contact_id)
                ->select(
                    'transactions.id',
                    'transaction_date',
                    'transactions.order_status',
                    'transactions.is_customer_order',
                    'invoice_no',
                    'contacts.name',
                    'bl.name as business_location',
                    'is_direct_sale'
                );
                
            if (!empty(request()->start_date) && !empty(request()->end_date)) {
                $start = request()->start_date;
                $end =  request()->end_date;
                $sells->whereDate('transaction_date', '>=', $start)
                            ->whereDate('transaction_date', '<=', $end);
            }
            $sells->groupBy('transactions.id');

            return Datatables::of($sells)
                ->addColumn(
                    'action',
                    '<a href="#" data-href="{{action(\'CustomerSellController@show\', [$id])}}" class="btn btn-xs btn-success btn-modal" data-container=".view_modal"><i class="fa fa-external-link" aria-hidden="true"></i> @lang("messages.view")</a>
                    @if($order_status == "waiting_for_confirmation") <button data-href="{{action(\'Ecom\EcomCustomerOrderController@confirmOrder\', [$id])}}" class="btn btn-xs btn-primary confirm_order"><i class="fa fa-check"></i>  @lang("lang_v1.confirm")</button> @endif
                    @if($order_status == "confirmed") <button data-href="{{action(\'Ecom\EcomCustomerOrderController@makeTheBill\', [$id])}}" class="btn btn-xs btn-primary make_the_bill"><i class="fa fa-note-o"></i>  @lang("lang_v1.make_the_bill")</button> @endif
                    &nbsp;
                <!--    @if($is_direct_sale == 1)
                        <a target="_blank" href="{{action(\'SellController@edit\', [$id])}}" class="btn btn-xs btn-primary"><i class="glyphicon glyphicon-edit"></i>  @lang("messages.edit")</a>
                    @else
                    <a target="_blank" href="{{action(\'SellPosController@edit\', [$id])}}" class="btn btn-xs btn-primary"><i class="glyphicon glyphicon-edit"></i>  @lang("messages.edit")</a>
                    @endif
 
                    &nbsp; 
                    <a href="#" class="print-invoice btn btn-xs btn-info" data-href="{{route(\'sell.printInvoice\', [$id])}}"><i class="fa fa-print" aria-hidden="true"></i> @lang("messages.print")</a>
                -->
                    &nbsp; <a href="{{action(\'SellPosController@destroy\', [$id])}}" class="btn btn-xs btn-danger delete-sale"><i class="fa fa-trash"></i>  @lang("messages.delete")</a>
                    '
                )
                ->removeColumn('id')
                ->editColumn('transaction_date', '{{@format_date($transaction_date)}}')
                ->editColumn(
                    'order_status',
                    '<span class="label @order_status($order_status)">{{__(\'lang_v1.\' . $order_status)}}
                        </span></span>'
                )
                ->setRowAttr([
                    'data-href' => function ($row) {
                        if (auth()->user()->can("sell.view")) {
                            return  action('SellController@show', [$row->id]) ;
                        } else {
                            return '';
                        }
                    }])
                ->rawColumns(['action', 'invoice_no', 'transaction_date', 'order_status'])
                ->make(true);
        }
        return view('customers.order-list');
    }
